












def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  difference = num1 - num2 
  difference
end

def division(num1, num2)
  dividing = num1 / num2
  dividing
end

def multiplication(num1, num2)
  multiply =  num1 * num2
  multiply
end

def modulo(num1, num2)
   modulo = num1 % num2
   modulo
end

def square_root(num)
  Math.sqrt(num)
end